def persian_aliases_for(name):
    """رشته‌ای از معادل‌های فارسی/فینگلیش برای یک نام بازی برمی‌گرداند (یا خالی)."""
    name_l = (name or '').lower()
    out = []
    for kw, aliases in _GAME_ALIASES.items():
        if kw in name_l:
            for a in aliases:
                if a not in out:
                    out.append(a)
    return ' '.join(out)
